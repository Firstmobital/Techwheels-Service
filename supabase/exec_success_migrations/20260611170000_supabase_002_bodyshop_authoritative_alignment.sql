-- 5) Trigger alignment
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM pg_trigger
    WHERE tgname = 'trg_bodyshop_assignments_updated_at'
      AND tgrelid = 'public.bodyshop_assignments'::regclass
  ) THEN
    CREATE TRIGGER trg_bodyshop_assignments_updated_at
    BEFORE UPDATE ON public.bodyshop_assignments
    FOR EACH ROW
    EXECUTE FUNCTION public.update_bodyshop_assignments_updated_at();
  END IF;
END
$$;

-- 6) Canonical module contract alignment (modules.name/label + user_module_permissions.module_id)
INSERT INTO public.modules (name, label, description, route, is_active)
VALUES (
  'bodyshop_repair',
  'Bodyshop Repair',
  'Track accidental vehicles through the full repair lifecycle',
  '/bodyshop-repair',
  true
)
ON CONFLICT (name)
DO UPDATE
SET
  label = EXCLUDED.label,
  description = EXCLUDED.description,
  route = COALESCE(public.modules.route, EXCLUDED.route),
  is_active = true;

WITH target_module AS (
  SELECT id AS module_id
  FROM public.modules
  WHERE name = 'bodyshop_repair'
), missing_rows AS (
  SELECT
    u.id AS user_id,
    tm.module_id
  FROM public.users u
  CROSS JOIN target_module tm
  LEFT JOIN public.user_module_permissions ump
    ON ump.user_id = u.id
   AND ump.module_id = tm.module_id
  WHERE u.is_active = true
    AND ump.id IS NULL
)
INSERT INTO public.user_module_permissions (user_id, module_id, can_view, can_modify, can_delete, granted_by, granted_at)
SELECT user_id, module_id, true, false, false, NULL::uuid, now()
FROM missing_rows
ON CONFLICT (user_id, module_id) DO NOTHING;

