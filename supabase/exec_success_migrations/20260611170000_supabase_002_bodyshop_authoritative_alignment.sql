-- 1) Core helper used by bodyshop_assignments trigger
CREATE OR REPLACE FUNCTION public.update_bodyshop_assignments_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

