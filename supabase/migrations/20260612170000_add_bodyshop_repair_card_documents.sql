BEGIN;

CREATE TABLE IF NOT EXISTS public.bodyshop_repair_card_documents (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  dealer_code text NOT NULL DEFAULT public.my_dealer_code(),
  repair_card_id bigint NOT NULL,
  reception_entry_id bigint,
  reg_number text,
  doc_key text NOT NULL,
  storage_bucket text NOT NULL DEFAULT 'autodoc'::text,
  storage_path text NOT NULL,
  file_name text,
  content_type text,
  file_size_bytes bigint,
  drive_url text,
  drive_file_id text,
  uploaded_by text NOT NULL DEFAULT COALESCE((auth.jwt() ->> 'email'::text), (auth.uid())::text, 'system'::text),
  uploaded_at timestamp with time zone NOT NULL DEFAULT now(),
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  updated_at timestamp with time zone NOT NULL DEFAULT now(),
  CONSTRAINT bodyshop_repair_card_documents_repair_card_fk
    FOREIGN KEY (repair_card_id) REFERENCES public.bodyshop_repair_cards(id) ON DELETE CASCADE,
  CONSTRAINT bodyshop_repair_card_documents_reception_fk
    FOREIGN KEY (reception_entry_id) REFERENCES public.service_reception_entries(id) ON DELETE SET NULL,
  CONSTRAINT bodyshop_repair_card_documents_doc_key_check CHECK (
    doc_key = ANY (ARRAY[
      'doc_claim_form'::text,
      'doc_rc'::text,
      'doc_insurance'::text,
      'doc_dl'::text,
      'doc_aadhaar'::text,
      'doc_pan'::text,
      'doc_kyc'::text,
      'doc_gst'::text,
      'doc_company_pan'::text,
      'doc_bank_detail'::text
    ])
  ),
  CONSTRAINT bodyshop_repair_card_documents_storage_path_not_blank CHECK (length(btrim(storage_path)) > 0)
);

CREATE UNIQUE INDEX IF NOT EXISTS ux_bodyshop_repair_card_documents_card_doc_key
  ON public.bodyshop_repair_card_documents (repair_card_id, doc_key);

CREATE INDEX IF NOT EXISTS idx_bodyshop_repair_card_documents_reception
  ON public.bodyshop_repair_card_documents (reception_entry_id);

CREATE INDEX IF NOT EXISTS idx_bodyshop_repair_card_documents_dealer
  ON public.bodyshop_repair_card_documents (dealer_code);

CREATE INDEX IF NOT EXISTS idx_bodyshop_repair_card_documents_uploaded_at
  ON public.bodyshop_repair_card_documents (uploaded_at DESC);

DROP TRIGGER IF EXISTS trg_bodyshop_repair_card_documents_updated_at ON public.bodyshop_repair_card_documents;
CREATE TRIGGER trg_bodyshop_repair_card_documents_updated_at
  BEFORE UPDATE ON public.bodyshop_repair_card_documents
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

ALTER TABLE public.bodyshop_repair_card_documents ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS admin_unrestricted_all_ops_v1 ON public.bodyshop_repair_card_documents;
CREATE POLICY admin_unrestricted_all_ops_v1
  ON public.bodyshop_repair_card_documents
  TO authenticated
  USING (public.is_admin())
  WITH CHECK (public.is_admin());

DROP POLICY IF EXISTS bodyshop_repair_card_documents_select_own_dealer ON public.bodyshop_repair_card_documents;
CREATE POLICY bodyshop_repair_card_documents_select_own_dealer
  ON public.bodyshop_repair_card_documents
  FOR SELECT
  TO authenticated
  USING (dealer_code = public.my_dealer_code());

DROP POLICY IF EXISTS bodyshop_repair_card_documents_insert_own_dealer ON public.bodyshop_repair_card_documents;
CREATE POLICY bodyshop_repair_card_documents_insert_own_dealer
  ON public.bodyshop_repair_card_documents
  FOR INSERT
  TO authenticated
  WITH CHECK (dealer_code = public.my_dealer_code());

DROP POLICY IF EXISTS bodyshop_repair_card_documents_update_own_dealer ON public.bodyshop_repair_card_documents;
CREATE POLICY bodyshop_repair_card_documents_update_own_dealer
  ON public.bodyshop_repair_card_documents
  FOR UPDATE
  TO authenticated
  USING (dealer_code = public.my_dealer_code())
  WITH CHECK (dealer_code = public.my_dealer_code());

DROP POLICY IF EXISTS bodyshop_repair_card_documents_delete_own_dealer ON public.bodyshop_repair_card_documents;
CREATE POLICY bodyshop_repair_card_documents_delete_own_dealer
  ON public.bodyshop_repair_card_documents
  FOR DELETE
  TO authenticated
  USING (dealer_code = public.my_dealer_code());

NOTIFY pgrst, 'reload schema';

COMMIT;
